x = "123456789"
print(x[0:2])
print(x[1:2])
print(x[2:4])
print(x[0:4])
print(x[1:8])
print(x[:2])
print(x[4:])
''' Podemos omitir índices, substituindo pelo extremo
correspondente e também podemos ter índices
negativos: -1 (último), -2 (penultimo). '''
print(x[4:-1])
print(x[-4:-1])
print(x[:])
